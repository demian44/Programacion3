<?php

abstract class Animalito implements IHtmlBody
{
    public function MakeHtml()
    {
        return "<DOCTYPE html><HEAD><TITLE>Animalito</TITLE></HEAD>";
    }
    public function MakeHeader()
    {
        return "<DOCTYPE html><HEAD><TITLE>Animalito</TITLE></HEAD>";
    }

    public function MakeBodyHtml()
    {
        return "<body></body>";
    }

}
